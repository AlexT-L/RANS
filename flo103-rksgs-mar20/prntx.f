      subroutine prntx (lprnt)
c
c     ******************************************************************
c     *                                                                *
c     *   prints the mesh                                              *
c     *                                                                *
c     ******************************************************************
c
      use dims
c
c     ******************************************************************
c
      use mesh_var
c
c     ******************************************************************
c
      implicit none
c
c     ******************************************************************
c
      integer  :: lprnt
c
c     ******************************************************************
c
c     local variables
c
c     ******************************************************************
c
      integer  :: iwrit
      integer  :: i,j
c
c     ******************************************************************
c
      iwrit     = 6

      write (iwrit,10)
   10 format(' mesh')

      do i=1,il,lprnt

      write (iwrit,12)
   12 format(3x,'  i ','  j ',
     .          '      x     ','      y     ','    area    ')

      do j=1,jl

      write (iwrit,14) i,j,x(i,j,1),x(i,j,2),vol(i+1,j+1)
   14 format(1x,2i4,3e12.4)

      end do
      end do

      return

      end
