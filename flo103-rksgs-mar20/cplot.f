      subroutine cplot
c
c     ******************************************************************
c     *                                                                *
c     *   plots cp at the mid points of the mesh intervals             *
c     *                                                                *
c     ******************************************************************
c
      use dims
c
c     ******************************************************************
c
      use out_var
      use mesh_var
c
c     ******************************************************************
c
      use flo_param
c
c     ******************************************************************
c
      implicit none
c
c     ******************************************************************
c
      integer  :: lprnt
c
c     ******************************************************************
c
c     local variables
c
c     ******************************************************************
c
      integer  :: iwrit
      integer  :: i,k
c
c     ******************************************************************
c
      real     :: cp1,xa
c
c     ******************************************************************
c
      real, dimension(2)       :: kode
      real, dimension(110)     :: line
c
c     ******************************************************************
c
      data        kode/' ','+'/
c
c     ******************************************************************
c
      iwrit     = 6
      cp1       = ((1.  +.2*rm**2)**3.5  -1.)/(.7*rm**2)

      write (iwrit,10)
   10 format(1x,'plot of cp')

      write (iwrit,12)
   12 format(3x,'    x   ','   cp   ')

      do i=1,110
         line(i)   = kode(1)
      end do

      do i=2,il
         xa        = .5*scal*(x(i,1,1)  +x(i-1,1,1))
         k         = 20.*(cp1  -cp(i))  +2.5
         k         = max(k,1)
         k         = min(k,110)
         line(k)   = kode(2)
         write (iwrit,22) xa,cp(i),line
         line(k)   = kode(1)
      end do

   22 format(1x,f10.4,f8.4,110a1)

      return

      end
